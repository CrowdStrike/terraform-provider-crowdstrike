const (
	dataConnectionTokenCreateTimeout  = 2 * time.Minute
	dataConnectionTokenCreateInterval = 5 * time.Second
)

// NewDataConnectionTokenResource creates a new NG-SIEM data connection token resource.
func NewDataConnectionTokenResource() resource.Resource {
	return &dataConnectionTokenResource{}
}

type dataConnectionTokenResource struct {
	client *client.CrowdStrikeAPISpecification
}

type dataConnectionTokenResourceModel struct {
	ConnectionID types.String      `tfsdk:"connection_id"`
	Triggers     types.Map         `tfsdk:"triggers"`
	Token        types.String      `tfsdk:"token"`
	IngestURL    types.String      `tfsdk:"ingest_url"`
	CreatedAt    timetypes.RFC3339 `tfsdk:"created_at"`
	ExpiresAt    timetypes.RFC3339 `tfsdk:"expires_at"`
}

