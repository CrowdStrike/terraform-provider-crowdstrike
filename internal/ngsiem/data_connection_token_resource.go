func (r *dataConnectionTokenResource) Create(
	ctx context.Context,
	req resource.CreateRequest,
	resp *resource.CreateResponse,
) {
	var plan dataConnectionTokenResourceModel
	resp.Diagnostics.Append(req.Plan.Get(ctx, &plan)...)
	if resp.Diagnostics.HasError() {
		return
	}

	token, diags := r.regenerateTokenWithRetry(ctx, plan.ConnectionID.ValueString())
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}

	resp.Diagnostics.Append(plan.wrap(*token)...)
	if resp.Diagnostics.HasError() {
		return
	}
	resp.Diagnostics.Append(resp.State.Set(ctx, &plan)...)
}

func (r *dataConnectionTokenResource) Read(
	ctx context.Context,
	req resource.ReadRequest,
	resp *resource.ReadResponse,
) {
	var state dataConnectionTokenResourceModel
	resp.Diagnostics.Append(req.State.Get(ctx, &state)...)
	if resp.Diagnostics.HasError() {
		return
	}

	// The token endpoints cannot be read without consuming/rotating the token,
	// so Read only verifies the underlying connection still exists. If it is
	// gone, the token is meaningless and the resource is removed from state.
	params := ngsiem.NewExternalGetDataConnectionByIDParams().
		WithContext(ctx).
		WithIds([]string{state.ConnectionID.ValueString()})

	res, err := r.client.Ngsiem.ExternalGetDataConnectionByID(params)
	if err != nil {
		diag := tferrors.NewDiagnosticFromAPIError(
			tferrors.Read,
			err,
			dataConnectionRequiredScopes,
		)
		if diag.Summary() == tferrors.NotFoundErrorSummary {
			resp.Diagnostics.Append(tferrors.NewResourceNotFoundWarningDiagnostic())
			resp.State.RemoveResource(ctx)
			return
		}
		resp.Diagnostics.Append(diag)
		return
	}

	if res == nil || res.Payload == nil || len(res.Payload.Resources) == 0 || res.Payload.Resources[0] == nil {
		resp.Diagnostics.Append(tferrors.NewResourceNotFoundWarningDiagnostic())
		resp.State.RemoveResource(ctx)
		return
	}

	resp.Diagnostics.Append(resp.State.Set(ctx, &state)...)
}

func (r *dataConnectionTokenResource) Update(
	ctx context.Context,
	req resource.UpdateRequest,
	resp *resource.UpdateResponse,
) {
	// All configurable attributes (connection_id, triggers) force
	// replacement, so Update never runs with a real change. Carry the prior
	// computed values forward.
	var plan dataConnectionTokenResourceModel
	resp.Diagnostics.Append(req.Plan.Get(ctx, &plan)...)
	if resp.Diagnostics.HasError() {
		return
	}
	resp.Diagnostics.Append(resp.State.Set(ctx, &plan)...)
}

func (r *dataConnectionTokenResource) Delete(
	_ context.Context,
	_ resource.DeleteRequest,
	_ *resource.DeleteResponse,
) {
	// There is no API to delete or revoke an ingest token independently. The
	// token remains valid server-side until the connection is deleted or the
	// token is regenerated. Removing the resource only drops it from state.
}

// regenerateTokenWithRetry calls the regenerate token endpoint, polling through
// the 202 ConnectionNotReady state until the connection is provisioned and a
// token is minted.
func (r *dataConnectionTokenResource) regenerateTokenWithRetry(
	ctx context.Context,
	connectionID string,
) (*models.DataconnectionmanagementConnectionToken, diag.Diagnostics) {
	var diags diag.Diagnostics
	var token *models.DataconnectionmanagementConnectionToken

	err := retry.RetryUntilNoError(
		ctx,
		dataConnectionTokenCreateTimeout,
		dataConnectionTokenCreateInterval,
		func() error {
			t, notReady, callDiags := r.regenerateToken(ctx, connectionID)
			if callDiags.HasError() {
				diags = callDiags
				return nil // stop retrying on hard error
			}
			if notReady {
				return fmt.Errorf("connection %s is not ready", connectionID)
			}
			token = t
			return nil
		},
	)
	if err != nil {
		diags.AddError(
			"Error generating data connection token",
			fmt.Sprintf("generating token for connection (%s): %s", connectionID, err),
		)
		return nil, diags
	}
	if diags.HasError() {
		return nil, diags
	}

	return token, diags
}

// regenerateToken performs a single call to the regenerate token endpoint. It
// returns notReady=true when the API responds 202 ConnectionNotReady.
func (r *dataConnectionTokenResource) regenerateToken(
	ctx context.Context,
	connectionID string,
) (*models.DataconnectionmanagementConnectionToken, bool, diag.Diagnostics) {
	var diags diag.Diagnostics

	params := ngsiem.NewExternalRegenerateDataConnectionTokenParams().
		WithContext(ctx).
		WithIds(connectionID)

	reader := &regenerateTokenReader{}
	_, accepted, err := r.client.Ngsiem.ExternalRegenerateDataConnectionToken(
		params,
		withRegenerateTokenReader(reader),
	)
	if err != nil {
		diags.Append(tferrors.NewDiagnosticFromAPIError(
			tferrors.Create,
			err,
			dataConnectionRequiredScopes,
		))
		return nil, false, diags
	}

	if accepted != nil {
		return nil, true, diags
	}

	if reader.response == nil {
		diags.Append(tferrors.NewEmptyResponseError(tferrors.Create))
		return nil, false, diags
	}

	if diag := tferrors.NewDiagnosticFromPayloadErrors(tferrors.Create, reader.response.Errors); diag != nil {
		diags.Append(diag)
		return nil, false, diags
	}

	if reader.response.Resources == nil || reader.response.Resources.Token == nil {
		diags.Append(tferrors.NewEmptyResponseError(tferrors.Create))
		return nil, false, diags
	}

	return reader.response.Resources, false, diags
}

// wrap maps the API token response onto the Terraform model. connection_id and
// triggers are plan-owned and left untouched.
func (m *dataConnectionTokenResourceModel) wrap(
	token models.DataconnectionmanagementConnectionToken,
) diag.Diagnostics {
	var diags diag.Diagnostics

	m.Token = flex.StringPointerToFramework(token.Token)
	m.IngestURL = flex.StringPointerToFramework(token.IngestURL)
	m.CreatedAt, diags = rfc3339FromPointer(token.CreatedAt)
	expiresAt, expiresDiags := rfc3339FromPointer(token.ExpiresAt)
	diags.Append(expiresDiags...)
	m.ExpiresAt = expiresAt

	return diags
}

// rfc3339FromPointer converts an API RFC3339 timestamp string into a
// timetypes.RFC3339, returning null for a nil or empty value.
func rfc3339FromPointer(value *string) (timetypes.RFC3339, diag.Diagnostics) {
	if value == nil || *value == "" {
		return timetypes.NewRFC3339Null(), nil
	}
	return timetypes.NewRFC3339Value(*value)
}
